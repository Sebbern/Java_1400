package oppgave1;

public class Oppgave1 {
    public static void main(String[] args) {
        int tall = 0;
        while (tall<25){
            tall++;
            System.out.println(tall+" ");
        }

        System.out.println();

        for (tall = 1; tall<=25; tall++){
            System.out.println(tall+" ");
        }
    }
}
